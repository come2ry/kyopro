def chmax(a: Any, b: Any) -> Tuple[Any, bool]:
    if (a < b):
        a = b  # aをbで更新
        return (a, True)
    return (a, False)


def chmin(a: Any, b: Any) -> Tuple[Any, bool]:
    if (a > b):
        a = b  # aをbで更新
        return (a, True)
    return (a, False)


def solve(N: int, A: "List[int]"):
    ans = [4] * N
    for i in range(len(A)):
        ans[A[i] - 1] -= 1

    for i in range(N):
        if ans[i] > 0:
            print(i + 1)
            break

    return


def main():
    def iterate_tokens():
        for line in sys.stdin:
            for word in line.split():
                yield word
    tokens = iterate_tokens()
    N = int(next(tokens))  # type: int
    A = [int(next(tokens)) for _ in range(4 * N - 1)]  # type: "List[int]"
    solve(N, A)


if __name__ == '__main__':
    main()
